    private void txtProductIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtProductIDKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            String pCode = txtProductID.getText();
            try
            {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/billingdb", "root", "");
                pst = con.prepareStatement("Select * from productdetails where productID = ?");	
		pst.setString(1, pCode);
		rs = pst.executeQuery();
                if(rs.next() == false)
		{
                    JOptionPane.showMessageDialog(this, "Product ID not Found");
		}
                else
                {
                    String name = rs.getString("Item");
                    String price = rs.getString("Price");
                    txtItem.setText(name);
                    txtPrice.setText(price);
                }
            }
            catch(Exception ex)
            {
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_txtProductIDKeyPressed

