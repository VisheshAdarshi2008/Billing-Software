    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel)tblProductBill.getModel();
        
        model.addRow(new Object[]
        {
            txtProductID.getText(),
            txtItem.getText(),
            txtQuantity.getValue().toString(),
            txtPrice.getText(),
            txtAmount.getText(),
        }
        );
        int sum = 0;
        for(int i = 0; i < tblProductBill.getRowCount(); i++)
        {
            sum = sum + Integer.parseInt(tblProductBill.getValueAt(i, 4).toString());
        }
        txtTotal.setText(Integer.toString(sum));
        txtProductID.setText("");
        txtItem.setText("");
        txtPrice.setText("");
        txtAmount.setText("");
        txtProductID.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed
    
    public void bill()
    {
        int tot = Integer.parseInt(txtTotal.getText());
        int pay = Integer.parseInt(txtPaid.getText());
        int bal = pay - tot;
        txtBalance.setText(String.valueOf(bal));
        String total = txtTotal.getText();
        String paid = txtPaid.getText();
        String balance = txtBalance.getText();
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel)tblProductBill.getModel();

        txtBill.setText(txtBill.getText() + "****************************************\n");
        txtBill.setText(txtBill.getText() + "*********BILLING DETAILS****************\n");
        txtBill.setText(txtBill.getText() + "****************************************\n\n");

        txtBill.setText(txtBill.getText() + "Product ID  \t Price  \t Amount  \n");
        for(int i = 0; i < tblProductBill.getRowCount(); i++)
        {
            String pid = (String)model.getValueAt(i, 0);
            String price = (String)model.getValueAt(i, 3);
            String amount= (String)model.getValueAt(i, 4);
            txtBill.setText(txtBill.getText() + pid + "\t\t  " + price + "\t\t  " + amount + "\n");
        }
        txtBill.setText(txtBill.getText() + "\n\n");
        txtBill.setText(txtBill.getText() + "\t Subtotal   :  " + total + "\n");
        txtBill.setText(txtBill.getText() + "\t Paid       :  " + paid + "\n");
        txtBill.setText(txtBill.getText() + "\t Balance    :  " + balance + "\n\n");
        txtBill.setText(txtBill.getText() + "****************************************\n");
        txtBill.setText(txtBill.getText() + "**********THANKS FOR VISITING***********\n");
        txtBill.setText(txtBill.getText() + "****************************************\n\n");

    }
    
